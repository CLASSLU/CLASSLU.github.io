const header = document.querySelector(".site-header");
const navToggle = document.querySelector(".nav-toggle");
const siteNav = document.querySelector("#siteNav");
const langButtons = Array.from(document.querySelectorAll("[data-lang-switch]"));
const revealElements = Array.from(document.querySelectorAll(".reveal"));
const reduceMotion = window.matchMedia("(prefers-reduced-motion: reduce)").matches;

function safeGetLanguage() {
  try {
    const value = localStorage.getItem("language");
    return value === "en" ? "en" : "zh";
  } catch (error) {
    return "zh";
  }
}

function safeSetLanguage(lang) {
  try {
    localStorage.setItem("language", lang);
  } catch (error) {
    // Ignore storage errors for private modes.
  }
}

function resolveText(key, lang) {
  return translations[lang][key] ?? translations.zh[key] ?? "";
}

function applyLanguage(lang) {
  const year = new Date().getFullYear();
  document.documentElement.lang = lang === "en" ? "en" : "zh-CN";
  document.title = resolveText("meta.title", lang);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const text = resolveText(key, lang).replace("{year}", String(year));
    element.textContent = text;
  });

  document.querySelectorAll("[data-i18n-attr]").forEach((element) => {
    const mapping = element.dataset.i18nAttr;
    if (!mapping) {
      return;
    }
    mapping.split(";").forEach((pair) => {
      const [attribute, key] = pair.split(":");
      if (!attribute || !key) {
        return;
      }
      element.setAttribute(attribute.trim(), resolveText(key.trim(), lang));
    });
  });

  langButtons.forEach((button) => {
    const isActive = button.dataset.langSwitch === lang;
    button.classList.toggle("is-active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  safeSetLanguage(lang);
}

function closeMobileMenu() {
  if (!siteNav || !navToggle) {
    return;
  }
  siteNav.classList.remove("is-open");
  navToggle.setAttribute("aria-expanded", "false");
}

function initNavigation() {
  if (!siteNav || !navToggle) {
    return;
  }

  navToggle.addEventListener("click", () => {
    const expanded = navToggle.getAttribute("aria-expanded") === "true";
    navToggle.setAttribute("aria-expanded", String(!expanded));
    siteNav.classList.toggle("is-open", !expanded);
  });

  siteNav.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", closeMobileMenu);
  });

  window.addEventListener(
    "resize",
    () => {
      if (window.innerWidth > 768) {
        closeMobileMenu();
      }
    },
    { passive: true }
  );
}

function initHeaderState() {
  if (!header) {
    return;
  }
  const onScroll = () => {
    header.classList.toggle("is-scrolled", window.scrollY > 14);
  };
  onScroll();
  window.addEventListener("scroll", onScroll, { passive: true });
}

function initReveal() {
  if (reduceMotion || typeof IntersectionObserver !== "function") {
    revealElements.forEach((element) => element.classList.add("is-visible"));
    return;
  }

  revealElements.forEach((element, index) => {
    element.style.setProperty("--reveal-delay", `${(index % 6) * 60}ms`);
  });

  const observer = new IntersectionObserver(
    (entries, currentObserver) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) {
          return;
        }
        entry.target.classList.add("is-visible");
        currentObserver.unobserve(entry.target);
      });
    },
    { threshold: 0.2, rootMargin: "0px 0px -8% 0px" }
  );

  revealElements.forEach((element) => observer.observe(element));
}

function initLanguageSwitch() {
  let currentLang = safeGetLanguage();
  applyLanguage(currentLang);

  langButtons.forEach((button) => {
    button.addEventListener("click", () => {
      const targetLang = button.dataset.langSwitch === "en" ? "en" : "zh";
      if (targetLang === currentLang) {
        return;
      }
      currentLang = targetLang;
      applyLanguage(currentLang);
      closeMobileMenu();
    });
  });
}

initLanguageSwitch();
initNavigation();
initHeaderState();
initReveal();
